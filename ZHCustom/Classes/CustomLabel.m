#import "CustomLabel.h"

@implementation CustomLabel

- (instancetype)initWithFrame:(CGRect)frame {
    if ([super initWithFrame:frame]) {
        self.backgroundColor = [UIColor purpleColor];
        self.textColor = [UIColor whiteColor];
    }
    return self;
}

@end
